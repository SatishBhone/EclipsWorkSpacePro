package pkg1;

public class ClassC {
     
  
	public static void main(String[] args)
	{		
          int a = 10; int b = 20; 
          int c = a+b;                   //addition
          System.out.println(c);
         int d = a-b;                 //subtraction
         System.out.println(d);
         int e = a*b;               // multiplication
         System.out.println(e);
         int f = a/b;              // division
         System.out.println(f);
         int g = ++a;               // Increment operator 
         System.out.println(g);
          int h = --a;
          System.out.println(h);    // decrement operator 
          int i = ++b;
          System.out.println(i);  // decrement operator 
          int j =--b;
          System.out.println(j);   // increment operator 
	}   

}
